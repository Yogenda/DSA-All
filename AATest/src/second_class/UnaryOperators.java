package second_class;

public class UnaryOperators {

	public static void main(String[] args) {
		int a=7;
		//pre increment/ decrement
		System.out.println(++a);
		System.out.println(--a);
		System.out.println((a=a+6)); // or a++
		
		
		//post increment/ decrement
		System.out.println(a++);
		System.out.println(a--);	
	}

}
