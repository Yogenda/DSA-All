package loops;

public class lp6 {

	public static void main(String[] args) {
		
		int x = 1;
		while(x<=5)
		{
			System.out.println(x + " Java Training...");
			x++;
		}
		
// for loop
		for(int n=1;n<=5;n++)
		{
			System.out.println(n + " Hello World");
		}
		
		
		System.out.println("-------------");
		for(int n=5;n>=1;n--)
		{
			System.out.println(n + " Hello World");
		}
		
		
	}
}
